    <h2>FAQ</h2>

    <details>
        <summary role="button" class="secondary">What is a URL shortener?</summary>
        <p>A URL shortener is a simple tool that takes a long URL and turns it into whatever URL you would like it to be.</p>
    </details>

    <details>
        <summary role="button" class="secondary">Is URL shortener safe?</summary>
        <p>Criminals will use shortened URLs to direct victims to phishing sites or initiate a download of malicious software on to your device. If you are suspicious of a shortened URL, don't click it.</p>
    </details>

    <details>
        <summary role="button" class="secondary">Do shortened URLs expire?</summary>
        <p>Many providers of shortened URLs claim that they will "never expire". However there is always the implied small print: so long as we do not decide to discontinue this service—there is no contract to be breached by a free service, regardless of "promises"—and remain in business).</p>
    </details>

    <details>
        <summary role="button" class="secondary">How do URL shorteners make money?</summary>
        <p>Many businesses will place advertisements on the URL shorteners. Thus, those URL shorteners can charge a monthly membership fee. They can also make money from advertisers because many advertisers will pay them to increase traffic to their websites or apps.</p>
    </details>

